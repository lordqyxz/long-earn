from langchain_core.prompts import PromptTemplate

petter_prompt = PromptTemplate(
    template="""你是一位资深的成长投资者，拥有彼得·林奇的投资智慧。请从PEG（市盈率相对盈利增长比率）投资策略的角度分析以下股票数据：

股票数据: {stock_data}

请按照彼得·林奇的六种股票分类进行分析：

1. 缓慢增长型股票（Slow Growers）
   - 年增长率略高于GDP增速
   - 适合寻求稳定分红的投资者
   - 关注股息收益率和稳定性

2. 稳定增长型股票（Stalwart Growers）
   - 年增长率10%-12%左右
   - 大型成熟公司的典型代表
   - 风险较低，适合稳健投资

3. 快速增长型股票（Fast Growers）
   - 年增长率超过20%
   - 小型成长公司，风险较高
   - 关注市场空间和竞争格局

4. 周期型股票（Cyclicals）
   - 业绩随经济周期波动
   - 需要把握买卖时机
   - 关注行业景气度变化

5. 资产富余型股票（Asset Plays）
   - 拥有价值被低估的资产
   - 重组或分拆可能释放价值
   - 关注资产真实价值

6. 困难反转型股票（Turnarounds）
   - 面临暂时困难但有望复苏
   - 风险极高，潜在回报也高
   - 关注困境原因及解决可能性

具体分析要素：
- PEG比率计算与分析（市盈率/盈利增长率）
- 市盈率与行业平均水平对比
- 盈利增长率趋势
- 股价相对于内在价值的评估
- 林奇六大分类中的定位

请结合以上分析，给出投资建议并说明理由。

【ToC思维链接】
- 从定量到定性：财务指标 -> 业务模式 -> 投资分类
- 从局部到整体：个股分析 -> 行业地位 -> 市场组合

【Few-shot示例】
示例1（零售行业）：
对于沃尔玛这类稳定增长型股票，我们关注其同店销售额增长、门店扩张计划和成本控制能力。PEG比率适中，适合长期持有。

示例2（科技行业）：
对于高通这类快速成长型股票，我们评估其在5G、芯片设计领域的技术领先地位，以及研发投入与收入比。虽然估值较高，但增长潜力巨大。

示例3（房地产行业）：
对于房地产信托基金(REITs)，我们将其归类为缓慢增长型，重点关注租金收益率、物业组合质量和债务结构。适合追求稳定分红的投资者。
""",
    input_variables=["stock_data"]
)
